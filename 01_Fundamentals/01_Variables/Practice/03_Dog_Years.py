# Scenario:
#
# A veterinarian wants a simple program that estimates a
# dog's age in "dog years."
#
# The dog's age is already known and stored in the program.
#
# For this exercise, assume:
#
#     1 human year = 7 dog years
#
# The program should calculate the dog's age in dog years
# and display the result.
#
# Assume all values are valid.
#
